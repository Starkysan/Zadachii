#include <iostream>
//Задача за извеждане на числа в обратен ред с цикъл while/do while/.
using namespace std;
int main()
{
	long int k; //създаване на променлива к.
	cout << "k=";
	cin >> k;
	while (k != 0)//Докато к е различно от нула задачите в цикъла ще се повтарят.
	{
		cout << k % 10;//Тази част служи за извеждане на последното число като се дел на 10 с остатък и се взима остатъкът, който е последното число.
		k = k / 10;//Тази част служи за премахване на последното число понеже, когато инт се дели на 10 остатъкът не се записва.Пример(54321 делено на 10 ще е 5432 и така за всяко следващо делене докато к не стане равно на нула).
	}
}


