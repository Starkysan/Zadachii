#include <iostream>
/* Да се състави програма, която въвежда в масив средния годишен успех на всеки ученик от един клас.
 Да се изведе най-високия успех.*/
using namespace std;
int main()
{
	double arr[30];//Създаване на масив от тип дабъл, в който ще се въвеждат оценките.
	int n;//Създаване на променлива, която ще служи за залагане на границата на въведените оценки. 
	cout << "Enter number of students: ";
	cin >> n;
	for (int i = 0; i < n; i++) //Създава се for  цикъл, който ще служи за въвеждането на оценките една по една.
	{
		cin >> arr[i];//Въвеждането става по следния начин - каквато е стойноста на i на тази позиция в масива ще се въведе оценката.
		// Тоест при i = 0 ще се въведе първата оценка, при i = 1 
		//ще се въведе втората и така нататък докато не стигне до 30 където ще се въведе последната оценка.
	}
	double max = arr[0];//Създаваме на променлива от тип дабъл , в която ще се складира най-високата от всички въведени от нас оценки.
	//Тази променлива ще е равна на първата въведена от нас оценка за да може сравнението да започне.
	for (int i = 1; i < n; i++) //Типичен фор цикъл който върти от второто до последното число на нашия списък с оценки.
	//(Започва от втората понеже променливата макс е вече равна на първата оценка)
	{
		if (arr[i] > max) //С помощта на фор цикъла можем да вземем всяка една стойност от списъка с оценки и да я сравним с променливата макс.
		//В случай , че някоя от тези оценки/стойности е по-голяма от max, max ще вземе нейната стойност.
		{
			max = arr[i];
		}
	}
	cout << max;
}

